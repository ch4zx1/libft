#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;
	size_t			i;

	str = (unsigned char *) b;
	i = 0;
	while (len > i)
	{
		str[i] = c;
		i++;
	}
	return (str);
}
